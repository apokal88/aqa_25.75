let radius = 34;
let square = Math.PI * (radius * radius);

console.log("The square of the circle is: " + square.toFixed(2));

// Task 4.2
let length = 5;
let width = 3;
let rectangleSquare = length * width;

console.log("The square of the rectangle is: " + rectangleSquare);

